  defp generate_string_by_parts(parts, max_count \\ 20) do
    count = Enum.random(1..max_count)

    1..count
    |> Enum.reduce("", fn _count, acc ->
      [acc | Enum.random(parts)]
    end)
    |> :erlang.iolist_to_binary()
  end
